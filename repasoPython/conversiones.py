#Conversión de datos
#Conversión de tipos numérico
# int()
x = int(3.14)
print(x)

# float()
y = float("2.5")
print(y)

# complex()
z = complex(3, 4)
print(z)

#Conversion tipos de datos---------------------------------
# str()
a = str(123)
print(a)

# list()
b = list((1, 2, 3))
print(b)

# tuple()
c = tuple([4, 5, 6])
print(c)

# set()
d = set([1, 1, 2, 2, 3, 3])
print(d)

# dict()
e = dict([('one', 1), ('two', 2)])
print(e)

#Conversion de booleanos------------------------
# bool()
f = bool(1)
print(f)

g = bool("")
print(g)

# int()
h = int(True)
print(h)

i = int(False)
print(i)

#Conversion tipos de secuencia
# bytearray()
j = bytearray("hello", "utf-8")
print(j)

# bytes()
k = bytes([0, 1, 2, 3])
print(k)
